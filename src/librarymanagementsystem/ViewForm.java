    private void updateBtn_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtn_bookActionPerformed
        // TODO add your handling code here:
        if (selectedBookId == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please edit a book first.");
            return;
        }
        try {
            String title = titleTextField.getText();
            String author = authorTextField.getText();
            int year = Integer.parseInt(yearTextField.getText());
            Genre selectedGenre = (Genre) genresComboBox.getSelectedItem();
            
            int selectedRow = bookTable.getSelectedRow();
            String status = (selectedRow != -1) ? bookTable.getValueAt(selectedRow, 5).toString() : "Available";
            
            Book updatedBook = new Book(selectedBookId, title, author, selectedGenre.getGenreId(), selectedGenre.getGenreName(), year, status);
            Database.updateBook(updatedBook);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Book Updated Successfully!");
            clearBtn_bookActionPerformed(evt);
            loadBooksTable();
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Year must be a valid number.", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtn_bookActionPerformed

