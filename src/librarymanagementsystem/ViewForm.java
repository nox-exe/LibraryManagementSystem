    private void returnBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBookBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = borrowRecordsTable.getSelectedRow();
        
        if (selectedRow != -1) {
            String returnStatus = borrowRecordsTable.getValueAt(selectedRow, 6).toString();
            
            if (!returnStatus.equals("Not Returned")) {
                javax.swing.JOptionPane.showMessageDialog(this, "This book has already been returned!");
                return;
            }
            
            int borrowId = (int) borrowRecordsTable.getValueAt(selectedRow, 0);
            int bookId = (int) borrowRecordsTable.getValueAt(selectedRow, 1);
            
            int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this, 
                "Process return for this book?", 
                "Confirm Return", 
                javax.swing.JOptionPane.YES_NO_OPTION
            );
            
            if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                java.sql.Date today = java.sql.Date.valueOf(java.time.LocalDate.now());
                
                boolean success = Database.returnBook(borrowId, bookId, today);
                
                if (success) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Book returned successfully!");
                    loadBorrowRecordsTable();
                    loadBooksTable(); 
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Database error: Could not process return.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a record from the log first.");
        }
    }//GEN-LAST:event_returnBookBtnActionPerformed

