    private void loadBooksTable() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"ID", "Title", "Author", "Genre", "Year", "Status"}, 0
        ){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        for (Book b : Database.getAllBooks()) {
            model.addRow(new Object[]{
                b.getBookId(), b.getTitle(), b.getAuthor(), b.getGenreName(), b.getPublicationYear(), b.getStatus()
            }); 
        }
        bookTable.setModel(model); 
    }
    
    private void loadBorrowersTable() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"ID", "First Name", "Last Name", "Contact Number"}, 0
        ){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        for (Borrower b : Database.getAllBorrowers()) {
            model.addRow(new Object[]{
                b.getBorrowerId(), b.getFirstName(), b.getLastName(), b.getContactNumber()
            }); 
        }
        bookTable1.setModel(model); 
    }
    
    private void loadBorrowRecordsTable() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"Record ID", "Book ID", "Book Title", "Patron Name", "Borrow Date", "Due Date", "Return Date"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        for (BorrowRecord br : Database.getAllBorrowRecords()) {
            String returnStatus = (br.getReturnDate() != null) ? br.getReturnDate().toString() : "Not Returned";
            
            model.addRow(new Object[]{
                br.getBorrowId(), br.getBookId(), br.getBookTitle(), br.getPatronName(),
                br.getBorrowDate(), br.getDueDate(), returnStatus
            }); 
        }
        borrowRecordsTable.setModel(model);
    }

