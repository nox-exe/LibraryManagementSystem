    private void addBtn_borrowerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtn_borrowerActionPerformed
        // TODO add your handling code here:
        try {
            String fName = fnameTextField.getText();
            String lName = lnameTextField.getText();
            String contact = contactNumTextField.getText();
            
            if (fName.isEmpty() || lName.isEmpty()) {
                throw new IllegalArgumentException("Names cannot be empty.");
            }
            
            Borrower newBorrower = new Borrower(fName, lName, contact);
            Database.addBorrower(newBorrower);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Borrower Added Successfully!");
            clearBtn_borrowerActionPerformed(evt);
            loadDropdowns();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error adding borrower: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtn_borrowerActionPerformed

