    private void loadDropdowns() {
        // Load Genres
        javax.swing.DefaultComboBoxModel genreModel = new javax.swing.DefaultComboBoxModel();
        for (Genre g : Database.getAllGenres()) {
            genreModel.addElement(g);
        }
        genresComboBox.setModel(genreModel);

        // Load ONLY Available Books for the Borrow tab
        javax.swing.DefaultComboBoxModel bookModel = new javax.swing.DefaultComboBoxModel();
        for (Book b : Database.getAllBooks()) {
            if ("Available".equals(b.getStatus())) {
                bookModel.addElement(b);
            }
        }
        bookComboBox.setModel(bookModel);

        // Load Borrowers
        javax.swing.DefaultComboBoxModel patronModel = new javax.swing.DefaultComboBoxModel();
        for (Borrower b : Database.getAllBorrowers()) {
            patronModel.addElement(b);
        }
        patronComboBox.setModel(patronModel);
    }
    
    private void setInitialDates() {
    java.time.LocalDate today = java.time.LocalDate.now();
    java.time.LocalDate dueDate = today.plusDays(7);

    borrowDateTextField.setText(today.toString()); 
    dueDateTextField.setText(dueDate.toString());
}

