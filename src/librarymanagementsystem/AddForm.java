    private void checkOutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutBtnActionPerformed
        // TODO add your handling code here:
        try {
            Book selectedBook = (Book) bookComboBox.getSelectedItem();
            Borrower selectedPatron = (Borrower) patronComboBox.getSelectedItem();
            
            if (selectedBook == null || selectedPatron == null) {
                throw new NullPointerException("Please ensure a Book and Patron are selected.");
            }
            
            java.sql.Date borrowDate = java.sql.Date.valueOf(borrowDateTextField.getText());
            java.sql.Date dueDate = java.sql.Date.valueOf(dueDateTextField.getText());
            
            boolean success = Database.borrowBook(selectedBook.getBookId(), selectedPatron.getBorrowerId(), borrowDate, dueDate);
            
            if (success) {
                javax.swing.JOptionPane.showMessageDialog(this, "Book Issued Successfully!");
                clearBtnActionPerformed(evt);
                loadDropdowns();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Database error: Could not process transaction.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (IllegalArgumentException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Date Error: Please use the YYYY-MM-DD format (e.g., 2026-07-24).", "Format Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_checkOutBtnActionPerformed

