    private void addBtn_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtn_bookActionPerformed
        // TODO add your handling code here:
        try {
            String title = titleTextField.getText();
            String author = authorTextField.getText();
            int year = Integer.parseInt(yearTextField.getText());
            
            Genre selectedGenre = (Genre) genresComboBox.getSelectedItem();
            
            Book newBook = new Book(title, author, selectedGenre.getGenreId(), year, "Available");
            Database.addBook(newBook);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Book Added Successfully!");
            clearBtn_bookActionPerformed(evt);
            loadDropdowns();
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Year must be a valid number.", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Please check all inputs.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtn_bookActionPerformed

